# ── Rate limiter (sliding-window, no external dependencies) ───────────────────

class _SlidingWindowRateLimiter:
    """Per-key sliding-window rate limiter using monotonic clock.

    Keeps a list of hit timestamps per key and trims them on every check.
    The memory footprint is bounded by limit * number_of_unique_keys.
    """

    def __init__(self, limit: int = 5, window: float = 60.0):
        self._limit = limit
        self._window = window
        self._hits: dict[str, list[float]] = defaultdict(list)

    def allow(self, key: str) -> bool:
        now = time.monotonic()
        cutoff = now - self._window
        hits = [t for t in self._hits[key] if t > cutoff]
        if len(hits) >= self._limit:
            self._hits[key] = hits
            return False
        hits.append(now)
        self._hits[key] = hits
        return True


# 5 generate requests per 60 s per authenticated user / IP
_rate_limiter = _SlidingWindowRateLimiter(limit=5, window=60.0)


@asynccontextmanager
async def lifespan(app: FastAPI):
    jobs_dir = os.environ.get("MUSEFORGE_JOBS_DIR", "/tmp/museforge_jobs")
    os.makedirs(jobs_dir, exist_ok=True)
    # Independent background tasks (disk orphans + stale DB job rows)
    from jobs import orphan_cleanup_loop, stale_job_reaper_loop

    cleanup_task = asyncio.create_task(orphan_cleanup_loop())
    reaper_task = asyncio.create_task(stale_job_reaper_loop())
    try:
        yield
    finally:
        cleanup_task.cancel()
        reaper_task.cancel()
        for task in (cleanup_task, reaper_task):
            try:
                await task
            except asyncio.CancelledError:
                pass


app = FastAPI(title="MuseForge API", version="2.3.0", lifespan=lifespan)

