@app.post("/api/generate", response_model=GenerateResponse)
async def generate(
    req: GenerateRequest,
    request: Request,
    background_tasks: BackgroundTasks,
    current_user: Optional[AuthUser] = Depends(get_optional_user),
):
    if req.director_style not in DIRECTOR_STYLES:
        raise HTTPException(status_code=400, detail=f"Unknown director style: {req.director_style}")

    # Rate-limit key: authenticated user_id preferred; fallback to client IP
    rl_key = (
        current_user.user_id
        if current_user
        else (request.client.host if request.client else "anon")
    )
    if not _rate_limiter.allow(rl_key):
        raise HTTPException(
            status_code=429,
            detail="Çok fazla istek. Lütfen 1 dakika bekleyin ve tekrar deneyin.",
            headers={"Retry-After": "60"},
        )

    demo = _is_demo()
    api_key = os.environ.get("MUAPI_KEY", "")

    # Block unauthenticated users outside demo mode
    if not demo and current_user is None:
        raise HTTPException(
            status_code=401,
            detail="Authentication required. Please sign in to generate videos.",
        )

    # Plan lookup, scene-limit enforcement, credit check — only for
    # authenticated non-demo requests. Demo/anonymous requests fall back to
    # the free-tier defaults (no music, watermark applies) and only the
    # Pydantic-level bounds on num_scenes.
    # When require_script_approval is True, credits are NOT charged here —
    # they are charged at approve-script time so the user can abandon a
    # bad script for free.
    plan = "free"
    music_enabled = False
    dialogue_enabled = False
    if current_user and not demo:
        plan = await _get_user_plan(current_user.user_id)

        max_scenes = PLAN_MAX_SCENES.get(plan, PLAN_MAX_SCENES["free"])
        if req.num_scenes > max_scenes:
            raise HTTPException(
                status_code=400,
                detail=f"Your plan allows up to {max_scenes} scenes. Upgrade to generate longer videos.",
            )

        # Optional background music — Creator/Pro only. Free/anonymous requests
        # that send music_enabled=True are silently ignored, not rejected.
        music_enabled = bool(req.music_enabled) and plan in ("creator", "pro")
        # Dialogue is intentionally Pro-only. Both the deployment-level feature
        # flag and the per-request opt-in must be enabled.
        dialogue_enabled = (
            bool(req.dialogue_enabled)
            and plan == "pro"
            and is_dialogue_enabled()
        )

        if not req.require_script_approval:
            credit_cost = build_credit_breakdown(
                req.num_scenes,
                music_enabled=music_enabled,
                dialogue_enabled=dialogue_enabled,
                plan=plan,
            )["total_credits"]
            ok = await _deduct_credits(current_user.user_id, credit_cost, "video_generation")
            if not ok:
                raise HTTPException(
                    status_code=402,
                    detail=f"Insufficient credits. You need {credit_cost} credits for this generation. Please top up your balance.",
                )

    # Pro-only library characters (name + static_features + portrait_url).
    # Free/Creator payloads that include this field are silently dropped.
    library_characters: List[dict] = []
    if current_user and not demo and plan == "pro" and req.library_characters:
        library_characters = [
            {
                "name": c.name.strip(),
                "static_features": c.static_features.strip(),
                "portrait_url": c.portrait_url.strip(),
            }
            for c in req.library_characters
            if c.name.strip() and c.static_features.strip() and c.portrait_url.strip()
        ]

    job = await job_store.create(
        idea=req.idea,
        style=req.style,
        director_style=req.director_style,
        aspect_ratio=req.aspect_ratio,
        num_scenes=req.num_scenes,
        user_requirement=req.user_requirement,
        demo=demo,
        user_id=current_user.user_id if current_user else None,
        user_email=current_user.email if current_user else None,
        character_image=req.character_image,
        character_name=req.character_name,
        music_enabled=music_enabled,
        dialogue_enabled=dialogue_enabled,
        plan=plan,
        require_script_approval=bool(req.require_script_approval),
        library_characters=library_characters,
    )

    background_tasks.add_task(run_generation_job, job, api_key)
    return GenerateResponse(job_id=job.id, demo=demo)


