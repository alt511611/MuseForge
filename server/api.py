def _cors_header_for(request: Request) -> dict:
    """Build the Access-Control-Allow-Origin header for a manual error
    response, matching the same allow_origins/allow_origin_regex rules as
    CORSMiddleware above.

    NOTE ON WHY THIS EXISTS: Starlette's CORSMiddleware does NOT add CORS
    headers to 500 responses caused by an unhandled exception -- those are
    generated by ServerErrorMiddleware, which sits OUTSIDE CORSMiddleware
    in the default stack. The practical effect: any unhandled backend
    exception shows up in the browser as a generic, misleading "blocked by
    CORS policy" error, hiding the real 500 and its message. This handler
    ensures every error response -- not just successful ones -- carries a
    correct CORS header, and surfaces the real error message to the client
    instead of a CORS red herring.
    """
    origin = request.headers.get("origin", "")
    if not origin:
        return {}
    if origin in ALLOWED_ORIGINS:
        return {"Access-Control-Allow-Origin": origin, "Vary": "Origin"}
    if ALLOWED_ORIGIN_REGEX and re.match(ALLOWED_ORIGIN_REGEX, origin):
        return {"Access-Control-Allow-Origin": origin, "Vary": "Origin"}
    return {}


@app.exception_handler(Exception)
async def _unhandled_exception_handler(request: Request, exc: Exception):
    logger.exception(f"Unhandled error on {request.method} {request.url.path}: {exc}")
    return JSONResponse(
        status_code=500,
        content={"detail": "Internal server error. Please try again."},
        headers=_cors_header_for(request),
    )


@app.middleware("http")
async def security_headers(request: Request, call_next):
    response = await call_next(request)
    response.headers["X-Content-Type-Options"] = "nosniff"
    response.headers["X-Frame-Options"] = "DENY"
    response.headers["Referrer-Policy"] = "strict-origin-when-cross-origin"
    response.headers["Strict-Transport-Security"] = "max-age=63072000; includeSubDomains"
    return response


